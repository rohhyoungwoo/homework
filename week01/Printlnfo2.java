package homework; //패키지 명

public class Printlnfo2 { //클래스의 중괄호 시작 영역
	public static void main(String[] args) { //main 메소드의 중괄호 시작 영역
		System.out.print(2024 + "년 " + 11 + "월 " + 22 + "일 " ); 
		//숫자와 문자 사이에 덧셈을 이용하여 값에 순서대로 2024, 년, 11, 월, 22, 일을 붙여서 출력하고 문자 뒤의 공백을 넣어 띄어쓰기도 포함하여 출력한다
		System.out.print(22 + "시 " + 20 + "분");
		//숫자와 문자 사이에 덧셈을 이용하여 값에 순서대로 22, 시, 20, 분을 붙여서 출력하고 문자 뒤의 공백을 넣어 띄어쓰기도 포함하여 출력한다
	} //main 메소드의 중괄호 끝 영역

} //클래스의 중괄호 끝 영역
